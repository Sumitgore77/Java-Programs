public class SimpleTrianglePat {
    public static void main(String x[]){

        for(int i=0; i<5;i++){
            
            for(int j=0; j<7;j++){
            if(j>=5-i){
           
                System.out.print(" ");
            }
        
            else{
                System.out.print("*");
            }
        }
        System.out.println();

        
    }
 
}
}

